<p class="header_3">The Quest for Cinchona – a Phylogenetic Tale</p>

<p>The objective is to use an iconic historical case to establish the predictive power of phylogenies as a new approach to infer the evolution of biochemical pathways or to select candidate organisms for drug discovery and traditional use. We will focus on Cinchona and relatives, which have been used to prevent and treat Malaria for hundreds of years. <a href="http://snm.ku.dk/forskning/sektioner/sektion_for_evolutionary_genomics/phylogenetic_prediction/the-quest-for-cinchona/" target="_blank">Read more here.</a></p>

<p>This project is co-ordinated by Nina Rønsted (Natural History Museum of Denmark).</p>

