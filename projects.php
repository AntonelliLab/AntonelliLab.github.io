  <p class="header_3">The Past, Present and Future of Neotropical Biodiversity</p>

<p>The American tropics – the Neotropics – comprise more species than any other region on Earth, including thousands of species used as crops, medicines and crafts. Understanding the evolution of this biodiversity and predicting the effects of climate and habitat changes on species losses constitute a major scientific challenge. This project will:</p> 

<p>1) Estimate the rates of historical migration, speciation and extinction among and within all major Neotropical biomes and regions, thereby identifying key areas for long-term conservation – such as those necessary for biotic interchange and vegetation shifts, and those that contain high levels of high genetic diversity.</p>

<p>2) Test competing hypotheses of speciation (soil specialisation, temperature increases, polyploidy, habitat shifts, range expansion) for the two main centres of Neotropical biodiversity: the tropical Andes and Amazonia. </p>

<p>3) Produce new estimates on species losses due to on-going climate and habitat changes based on our new findings in 1) and 2) above. </p>

<p>To achieve these goals we are developing novel bioinformatic pipelines that will greatly improve our use of biological databases. We will analyse DNA sequences, georeferences and biotic traits for tens of thousands of plant and animal species. Our tools will enable continuously up-to-date inferences and allow the easy integration of new data by students and researchers (several of those in our lab) interested in the evolution of particular species groups or biomes. </p>

<p><strong>FUNDING: </strong>European Research Council, Swedish Research Council</p>


