      differences in biodiversity between the two continents as an
      “evolutionary experiment”. To understand this dichotomy,
      Alexander is combining cross-taxonomic analyses of large scale
      biodiversity patterns (including plants and animals) with the
      analysis of niche evolution, ancestral  area reconstruction and
      diversification rate analyses in  focal plant groups.</p> 
