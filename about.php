<br>
<div class="header_4">OUR VISION</div> 
<br>
<br>
 
<p><i>To be one of the most <b>creative, productive </b>and <b>exciting </b>research groups in evolutionary biology and biogeography. </i></p>

<p>In other words:<i> We aim to do <b>great science </b>and have <b>a lot of fun</b>!</i></p>
<br>

<div class="hr_4"></div>
<div class="header_4">CORE VALUES</div> 
<br>
<br>
<br>
<p><b>Quality.</b> We prioritize quality above quantity in scientific publications, by investing on generating large data sets and performing robust analyses to tackle outstanding problems. We often target inter-disciplinary, and whenever possible, highly ranked scientific journals.</p>

<p><b>Innovation.</b> We develop new methodological solutions, challenge old assumptions, test competing hypotheses, and experiment with new techniques. We brainstorm on each research project and design papers on an individual basis. </p>

<p><b>Openness.</b> We share data, ideas, unpublished manuscripts and research applications within the group to foster cross-pollination of ideas and increase internal collaborations. At the same time, we take intellectual property and personal integrity seriously. </p>

<p><b>Commitment. </b>We love what we do and take full responsibility for our commitments, working actively and enthusiastically to achieve our goals and advance our science.</p>
<br>
<div class="hr_4"></div>
<div class="header_4">THE ANTONELLI LAB IN A NUTSHELL</div> 
<br>
<br>
<p><b>Who are we?</b></p>

<p>We are a research group comprising different <i>academic and technical categories</i> (associate professors, post-docs, students at PhD, MSc and BSc levels, bioinformaticians, laboratory technicians, research assistants) with <i>various scientific backgrounds</i> (biology, ecology, computational biology, geology, botany, zoology). We represent many <i>nationalities</i> (Brazilian, German, Dutch, Italian, American, French, Bolivian, Colombian, Swedish, Thai, Spanish, Finnish), work with many different <i>scientific questions</i> (in molecular phylogenetics, macroevolution, biogeography, population genetics, climate change, taxonomy, palaeontology), <i>organism groups</i> (plants, birds, frogs, insects, mammals, snakes, microorganisms) and <i>approaches</i> (empirical, theoretical and methodological). </p>
<br>
<p><b>What unifies our group?</b></p>

<p>Despite our breadth of backgrounds and interests, we are united by a genuine interest in understanding <b>how biological diversity has evolved, how it varies over space and time, </b>and<b> which factors and processes determine this variation</b>. </p>
<br>
<p><b>How are we structured?</b></p>

<p>We consist of two main categories: <i>core participants</i> (those funded through the PI's grant at the <a href="http://www.gu.se">University of Gothenburg</a>, or [for MSc students] having the university as their primary institution) and <i>associated researchers and students </i>(those actively collaborating with the group but employed or registered elsewhere). Associated members spend at least some time at the <a href="http://www.bioenv.gu.se">Department of Biological and Environmental Sciences</a> (located within the <a href="http://www.gotbot.se">Gothenburg botanical garden</a>) through internships or research visits. </p>
<br>

<div class="hr_4"></div>
<div class="header_4">AVAILABLE RESOURCES</div> 
<br>
<br>
<p>Most resources are shared and subject to common rules and praxis. These include, among others:<br />
<br />
<b>State-of-the-art DNA labs and knowledge.</b> We have access to several rooms for DNA and RNA extraction, quantification (through electrophoresis, TapeStation and Nanodrop machines), amplification and PCR. We are moving away from Sanger DNA sequencing (whereby only a few genetic markers are sequenced) to next-generation sequencing (NGS) techniques, resulting in vast amounts of genetic data at relatively low costs (after suitable protocols and probes have been designed and tested, which are often taxon-specific). We are performing sequence capture (targeting genes using taxon-specific probes), generating transcriptomes, ultra-conservative elements (UCEs), and full genomes. We have extensive in-house experience with library preparation, barcoding and multiplexing, and hybrid gene capture. These resources are part of the <a href="http://http://bioenv.gu.se/english/research/research-resources/nosi">Non-model Organism Sequencing Infrastructure (NOSI)</a> at our department.<br />
<br />
<b>Genomic infrastructures.</b> We use the two national genomic facilities, <a href="http://http://www.scilifelab.se/">SciLifeLab</a> (Stockholm &amp; Uppsala) and the <a href="http://http://cf.gu.se/english/genomics">Sahlgrenska Academy Core Facility</a> (Gothenburg) for genomic sonication and sequencing (mostly utilising the Illumina MySeq and HiSeq platforms). At SciLifeLab we have the advantage of not having to pay for labour (whose costs are covered by the Swedish Research Council, and regularly amount to c. 40% of total sequencing costs). <br />
<br />
<b>Computational resources.</b> We use our own computers to design and set up analyses, but then run heavier computational tasks on external resources, selected in relation to the specific needs. First off, we have a shared MacPro (3.5 GHz 6-Core Intel Xeon, 32 GB memory) called spacemule. For more demanding tasks, we use <a href="http://http://www.matstopel.se/albiorix">Albiorix</a>, which is a set of interconnected computers (c. 100 nodes) dedicated to high-performance bioscience computing physically located at our department. For more complex tasks we have accounts at the <a href="http://http://www.snic.se/">Swedish National Infrastructure for Computing</a>. We have access to bioinformatic expertise both within the group (planned to increase in January 2016 through the hiring of a bioinformatician working 50% for us) and through the Swedish national <a href="http://http://bils.se/">Bioinformatic Infrastructure for Life Sciences</a>. We are also working towards increasing bioinformatic knowledge among group members through on-demand workshops and mini-courses (e.g. in Unix, databases, R, genomic annotation). <br />
<br />
<b>Natural history and living collections.</b> Our department hosts the <a href="http://http://bioenv.gu.se/english/research/research-resources/Herbarium/%3FlanguageId=100001%26disableRedirect=true%26returnUrl=http%253A%252F%252Fbioenv.gu.se%252Fforskning%252Fforskningsresurser%252Fherbarium%252F">Herbarium GB</a>, housing c. 1 million dried plant specimens. A large proportion of these collections were made in South America, through the work of dozens of researchers and students, often in connection with the long-standing <a href="http://http://www2.dpes.gu.se/project/ecuador/">Flora of Ecuador</a> project coordinated from our department. <a href="http://www.gotbot.se">Gothenburg botanical garden</a> is one of the largest botanical gardens in Europe with an area of 175 hectares, where about 15000 species and cultivars are grown and of free access to researchers. The <a href="http://http://www.goteborg.com/en/goteborgs-naturhistoriska-museum/">Natural History Museum in Gothenburg</a> and the <a href="http://http://www.nrm.se/">Swedish Museum of Natural History</a> in Stockholm house together tens of millions of animal and plant specimens that are often used by members in our group for taxonomic purposes and as genetic resources. We also have extensive collaboration with many other natural history and living collections around Europe and worldwide. </p>
<br>
<div class="hr_4"></div>
<div class="header_4">INTERESTED IN JOINING OUR GROUP?</div> 
<br>
<br>
<p>We are always looking for highly motivated students and researchers at any career stage to spend time at our lab and interact with us. However, we can only offer a limited number of openings due to space and time constraints. We will therefore prioritize those people and projects that we judge have the <i>highest potential for scientific impact</i> and/or that best <i>fit or complement our group's skills, activities and interests</i>. Other aspects that we will take into consideration are the applicant's <i>communication skills</i> (English is our working language), <i>CV</i>, <i>independence of working</i>, and <i>own funding</i>. </p>

<p>A pre-condition for joining the group is that you are aware of, and fully agree with, the way we work and the commitment we expect from all members of the lab. You will therefore be asked to read and sign our <a href="pdf/Working_agreement.doc"<i>working agreement</i></a>. </p>

<p>If you feel qualified and agree with the above, do get in touch to discuss potential collaborations and a possible affiliation to our group! You will be asked to provide a <a href="https://docs.google.com/document/d/1rFbFpJzvZ0X4YTsahKM_0Txsi5zOqxQKPj2aC_KY9Ho/edit?usp=sharing"<i>one-page outline</i></a> of your main research questions and how you plan to develop them. Please send this together with your CV to <a href="mailto:alexandre.antonelli@bioenv.gu.se">Alexandre Antonelli</a>.</p>

