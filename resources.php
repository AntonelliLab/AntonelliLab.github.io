<div id="content_resources">

<p> <strong>PyRate</strong> is a Python program to estimate speciation, extinction, and preservation rates from fossil occurrence data using probabilistic framework to jointly estimate species-specific times of speciation and extinction and the rates of the underlying birth-death process based on the fossil record. The rates are allowed to vary through time independently of each other, and the probability of preservation and sampling is explicitly incorporated in the model to estimate the true lifespan of each lineage. Different Bayesian algorithms are available to assess the presence of temporal rate shifts by exploring alternative diversification models.
The program can be downloaded <a href="https://github.com/dsilvestro/PyRate" target="_blank" >from the GitHub repository</a>. 
The methods and program are described <a href="http://sysbio.oxfordjournals.org/content/63/3/349"  target="_blank">here</a> and <a href="http://onlinelibrary.wiley.com/doi/10.1111/2041-210X.12263/full" target="_blank">here</a>.</p>

<div class="hr"></div>
 
<p><strong>SUPERSMART </strong> (Self-Updating Platform for Estimating Rates of Speciation and Migration, Ages and Relationships of Taxa). The programming code is constantly updated and available on <a href="http://www.supersmart-project.org/" target="_blank">The SUPERSMART homepage</a>. A full manuscript describing the project and its bioinformatic components is provided in this <a href="https://peerj.com/preprints/501/" target="_blank">Pre-Print</a>.</p>
<div class="hr"></div>

<p><strong>SpeciesGeoCoder</strong> We have developed a software for coding species into user-defined units for e.g. biogeographic analyses, using a combination of GIS polygons and altitudinal ranges. There are two main packages: one written in python and another in R. The python code is described in a <a href="http://biorxiv.org/content/early/2014/09/17/009274" target="_blank">pre-print</a> and is available at <a href="https://github.com/mtop/speciesgeocoder/releases" target="_blank">github</a> with a simplified Graphical User Interface (GUI) available from <a href="http://sourceforge.net/projects/speciesgeocodergui" target="_blank">sourceforge</a>; see more details on the package's <a href="https://github.com/mtop/speciesgeocoder/wiki" target="_blank">wiki page</a>. The R package is available from <a href="https://cran.rstudio.com/web/packages/speciesgeocodeR/" target="_blank">CRAN</a>.
</p>

<div class="hr"></div>

<p><strong>Bioregion delimitation.</strong> Daril Vilhena and Alexandre Antonelli recently described a network method for delimiting biogeographical regions based on species occurrence data (Vilhena and Antonelli 2015, Nature Communications). The tool has since then been further developed and implemented <a href="http://mapequation.org/bioregions" target="_blank">here</a> and is fully described in a freely available <a href="http://arxiv.org/abs/1512.00892" target="_blank">pre-print</a>. </p>
<div class="hr"></div>

